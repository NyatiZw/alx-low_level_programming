#include "main.h"

/**
 * print_diagonal- entry point
 *
 * Description:- 'function that draws a diagonal line'
 * @n:- number of times line is printed
 *
 * Return: void
 */

void print_diagonal(int n)
{
	n = '92';

	while (n == '92'){
		for (n = '92'; n <= '92';)
		{
			if (n = 0)
			{
				_putchar('\n');
			}
		}
		n++;
	}
}
