/**
 * is_prime_number- Entry Point
 *
 * Description:- 'A function that returns prime numbers'
 * @n:- number
 *
 * Return: 1 (Success) 0 (Not Prime Number)
 */

int is_prime_number(int n)
{
	int m = 0, x;

	m = n / x;

	if (n % m != 0)
	{
		return (1);
	}
	if (x == 1 && x == n)
	{
		return (0);
	}
	return (is_prime_number);
}
