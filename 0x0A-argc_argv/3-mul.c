#include <stdio.h>
#include <stdlib.h>

/**
 * main- entry point
 *
 * Description:- ' Function that multiplies two numbers'
 *
 * @argc:- arguments count
 * @argv:- arguments vector
 *
 * Return: Always (0)
 */

int main(int argc, char *argv[])
{
	int a = 0, b, res;

	if (a * b)
	{
		printf("%d * %d = %d\n", a, b, res);
	}
	else
	{
		printf("Error");
		return (1);
	}
	return (0);
}
